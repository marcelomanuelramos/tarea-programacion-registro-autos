export class Auto {

    marca: string;
    modelo: string;
    motor: number;
    patente: string;
    nroDeChasis: number;
   

    constructor(marca: string,  modelo: string, motor: number, patente: string,  nroDeChasis: number) {
        this.marca = marca;
        this.modelo = modelo;
        this.motor = motor;
        this.patente = patente;
        this.nroDeChasis = nroDeChasis;
        
    }
}
